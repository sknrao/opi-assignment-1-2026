// OPIDPUSetReconciler reconciles OPIDPUSet objects by translating them into
// DPF CRDs via the DPFAdapter and mapping status back.
//
// NOTE: This reconciler is registered strictly for the NVIDIA provider. The
// main OPI operator owns higher-level routing and dispatches to vendor-specific
// reconcilers (like this one) based on the spec.vendor field.
type OPIDPUSetReconciler struct {
	client.Client
	Scheme       *runtime.Scheme
	Adapter      DPFAdapter
	StatusMapper *StatusMapper
}

// Reconcile implements the phase-gated state machine for OPIDPUSet resources.
// Each phase is a gate: the reconciler exits and requeues after each phase
// until the gate condition is met, preventing reconciliation thrashing during
// multi-gigabyte BFB downloads and physical RSHIM driver interactions.
func (r *OPIDPUSetReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	logger := log.FromContext(ctx)
	logger.Info("Reconciling OPIDPUSet", "name", req.NamespacedName)

	// 1. Fetch the OPIDPUSet resource.
	opiDPUSet := &OPIDPUSet{}
	if err := r.Get(ctx, req.NamespacedName, opiDPUSet); err != nil {
		return ctrl.Result{}, client.IgnoreNotFound(err)
	}

	// 2. Vendor dispatch: only handle NVIDIA.
	if opiDPUSet.Spec.Vendor != "nvidia" {
		logger.Info("Skipping non-NVIDIA OPIDPUSet", "vendor", opiDPUSet.Spec.Vendor)
		return ctrl.Result{}, nil
	}

	// 3. Handle deletion with finalizer.
	if !opiDPUSet.DeletionTimestamp.IsZero() {
		if controllerutil.ContainsFinalizer(opiDPUSet, OPIFinalizer) {
			if err := r.Adapter.DeleteDPFResources(ctx, opiDPUSet); err != nil {
				logger.Error(err, "Failed to delete DPF resources")
				return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
			}
			controllerutil.RemoveFinalizer(opiDPUSet, OPIFinalizer)
			if err := r.Update(ctx, opiDPUSet); err != nil {
				return ctrl.Result{}, err
			}
		}
		return ctrl.Result{}, nil
	}

	// 4. Ensure finalizer is present.
	if !controllerutil.ContainsFinalizer(opiDPUSet, OPIFinalizer) {
		controllerutil.AddFinalizer(opiDPUSet, OPIFinalizer)
		if err := r.Update(ctx, opiDPUSet); err != nil {
			return ctrl.Result{}, err
		}
	}

	// ---------------------------------------------------------------
	// Phase-Gated State Machine
	// Each phase gate exits early with a requeue if the gate condition
	// is not met, preventing concurrent orchestration thrashing.
	// ---------------------------------------------------------------

	// Phase 1: BootImageMapping — Validate and assert BFB resource state.
	// Gate: BFB must report Ready before we proceed to DPUSet creation.
	bfb, err := r.Adapter.EnsureBFB(ctx, opiDPUSet)
	if err != nil {
		if isUnrecoverableError(err) {
			return r.setTerminalError(ctx, opiDPUSet, "BFBValidationFailed", err)
		}
		logger.Error(err, "Failed to ensure BFB")
		r.setErrorStatus(ctx, opiDPUSet, "BFBSyncFailed", err.Error())
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}

	// Check BFB gate: BFB must be Ready before proceeding.
	if bfb.Status.Phase != "Ready" {
		logger.Info("BFB not yet ready, holding at BootImageMapping gate", "bfbPhase", bfb.Status.Phase)
		opiDPUSet.Status.Phase = PhaseBootImageMapping
		opiDPUSet.Status.Conditions = []metav1.Condition{{
			Type:               "BFBReady",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: metav1.Now(),
			Reason:             "WaitingForBFBRegistration",
			Message:            fmt.Sprintf("BFB image metadata not yet registered (current: %s)", bfb.Status.Phase),
		}}
		_ = r.Status().Update(ctx, opiDPUSet)
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, nil
	}

	// Phase 2: ClusterIsolation — Trigger DPUCluster orchestration.
	// Gate: DPUCluster must be Ready before DPUSet provisioning.
	dpuCluster, err := r.Adapter.EnsureDPUCluster(ctx, opiDPUSet)
	if err != nil {
		if isUnrecoverableError(err) {
			return r.setTerminalError(ctx, opiDPUSet, "DPUClusterValidationFailed", err)
		}
		logger.Error(err, "Failed to ensure DPUCluster")
		r.setErrorStatus(ctx, opiDPUSet, "DPUClusterSyncFailed", err.Error())
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}

	// If DPUCluster is required but not yet initialized, hold at gate.
	if dpuCluster != nil {
		// Stub: in real impl, check dpuCluster.Status.Phase
		// For now, assume cluster needs initialization.
		logger.Info("DPUCluster created, checking initialization", "name", dpuCluster.Name)
		opiDPUSet.Status.Phase = PhaseClusterIsolation
		// In production, we would check dpuCluster.Status.Phase == "Ready"
		// and requeue if not. For skeleton, we proceed.
	}

	// Phase 3: RollingRollout — Apply DPUSet to trigger physical flashing.
	dpuSet, err := r.Adapter.EnsureDPUSet(ctx, opiDPUSet, bfb)
	if err != nil {
		if isUnrecoverableError(err) {
			return r.setTerminalError(ctx, opiDPUSet, "DPUSetValidationFailed", err)
		}
		logger.Error(err, "Failed to ensure DPUSet")
		r.setErrorStatus(ctx, opiDPUSet, "DPUSetSyncFailed", err.Error())
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}
	_ = dpuSet // Used for owner reference in real impl

	// Sync status from DPF DPUSet back to OPI.
	dpfDPUSetName := opiDPUSet.Name + "-dpf"
	dpfStatus, err := r.Adapter.GetDPUSetStatus(ctx, dpfDPUSetName, DPFNamespace)
	if err != nil {
		logger.Error(err, "Failed to get DPUSet status from DPF")
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}

	opiPhase, conditions := r.StatusMapper.MapDPUSetPhase(*dpfStatus)

	// Handle hardware cold-boot / PCIe reset: exit gracefully WITHOUT error
	// to prevent controller-manager exponential-backoff thrashing on normal
	// hardware lifecycle events.
	if dpfStatus.Phase == "AwaitingReboot" {
		logger.Info("Hardware PCIe cold-boot required, parking at AwaitingHardwareReset")
		opiDPUSet.Status.Phase = PhaseAwaitingHardwareReset
		opiDPUSet.Status.ReadyCount = dpfStatus.ReadyCount
		opiDPUSet.Status.DPUCount = dpfStatus.TotalCount
		opiDPUSet.Status.Conditions = []metav1.Condition{{
			Type:               "HardwareResetRequired",
			Status:             metav1.ConditionTrue,
			LastTransitionTime: metav1.Now(),
			Reason:             "PCIeColdBootPending",
			Message:            "One or more DPUs require a physical PCIe cold-boot reset",
		}}
		_ = r.Status().Update(ctx, opiDPUSet)
		// Return nil error: this is a normal hardware lifecycle event.
		return ctrl.Result{RequeueAfter: HardwareRequeueInterval}, nil
	}

	// Map DPF phase to OPI phase.
	if opiPhase != PhaseReady {
		opiDPUSet.Status.Phase = PhaseRollingRollout
	} else {
		opiDPUSet.Status.Phase = PhaseReady
	}
	opiDPUSet.Status.ReadyCount = dpfStatus.ReadyCount
	opiDPUSet.Status.DPUCount = dpfStatus.TotalCount
	opiDPUSet.Status.Conditions = conditions
	opiDPUSet.Status.VendorStatus = &NvidiaVendorStatus{
		DPFDPUSetName:  dpfDPUSetName,
		DPFDPUSetPhase: dpfStatus.Phase,
	}

	// Add the synced condition.
	opiDPUSet.Status.Conditions = append(opiDPUSet.Status.Conditions, metav1.Condition{
		Type:               "DPFResourcesSynced",
		Status:             metav1.ConditionTrue,
		LastTransitionTime: metav1.Now(),
		Reason:             "SyncSuccessful",
		Message:            "All DPF resources are synced",
	})

	if err := r.Status().Update(ctx, opiDPUSet); err != nil {
		logger.Error(err, "Failed to update OPIDPUSet status")
		return ctrl.Result{}, err
	}

	// Requeue if not yet ready to keep polling DPF status.
	if opiDPUSet.Status.Phase != PhaseReady {
		logger.Info("DPUs not yet ready, requeuing", "phase", opiDPUSet.Status.Phase)
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, nil
	}

	logger.Info("OPIDPUSet reconciliation complete", "phase", opiDPUSet.Status.Phase)
	return ctrl.Result{}, nil
}

// setErrorStatus is a helper to update the OPIDPUSet status on retryable error.
func (r *OPIDPUSetReconciler) setErrorStatus(ctx context.Context, obj *OPIDPUSet, reason, message string) {
	obj.Status.Phase = PhaseError
	obj.Status.Conditions = []metav1.Condition{
		{
			Type:               "DPFResourcesSynced",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: metav1.Now(),
			Reason:             reason,
			Message:            message,
		},
	}
	_ = r.Status().Update(ctx, obj)
}

// setTerminalError implements the Status-Commit-Before-TerminalError protocol.
// It commits the failure condition to etcd BEFORE signaling a terminal halt,
// preventing the controller manager from dropping state logs during immediate
// execution halt. This guarantees platform visibility for unrecoverable errors.
func (r *OPIDPUSetReconciler) setTerminalError(ctx context.Context, obj *OPIDPUSet, reason string, originalErr error) (ctrl.Result, error) {
	logger := log.FromContext(ctx)
	logger.Error(originalErr, "Unrecoverable error detected, committing status before terminal halt", "reason", reason)

	// Step 1: Commit status BEFORE signaling terminal halt.
	obj.Status.Phase = PhaseError
	obj.Status.Conditions = []metav1.Condition{
		{
			Type:               "HardwareValidationFailed",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: metav1.Now(),
			Reason:             reason,
			Message:            originalErr.Error(),
		},
	}

	if statusErr := r.Status().Update(ctx, obj); statusErr != nil {
		// Status patch failed — return retryable error so we can try again.
		// Do NOT signal terminal halt if we failed to persist the reason.
		logger.Error(statusErr, "Failed to commit error status, will retry")
		return ctrl.Result{}, statusErr
	}

	// Step 2: Only after status is committed, signal terminal halt.
	return ctrl.Result{}, reconcile.TerminalError(originalErr)
}

