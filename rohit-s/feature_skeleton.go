// OPIDPUServiceReconciler reconciles OPIDPUService objects by translating them
// into DPF DPUService CRDs and mapping status back.
type OPIDPUServiceReconciler struct {
	client.Client
	Scheme       *runtime.Scheme
	Adapter      DPFAdapter
	StatusMapper *StatusMapper
}

// Reconcile is the main reconciliation loop for OPIDPUService resources.
func (r *OPIDPUServiceReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	logger := log.FromContext(ctx)
	logger.Info("Reconciling OPIDPUService", "name", req.NamespacedName)

	// 1. Fetch the OPIDPUService resource.
	opiSvc := &OPIDPUService{}
	if err := r.Get(ctx, req.NamespacedName, opiSvc); err != nil {
		return ctrl.Result{}, client.IgnoreNotFound(err)
	}

	// 2. Vendor dispatch.
	if opiSvc.Spec.Vendor != "nvidia" {
		logger.Info("Skipping non-NVIDIA OPIDPUService", "vendor", opiSvc.Spec.Vendor)
		return ctrl.Result{}, nil
	}

	// 3. Handle deletion.
	if !opiSvc.DeletionTimestamp.IsZero() {
		if controllerutil.ContainsFinalizer(opiSvc, OPIFinalizer) {
			if err := r.Adapter.DeleteDPFResources(ctx, opiSvc); err != nil {
				return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
			}
			controllerutil.RemoveFinalizer(opiSvc, OPIFinalizer)
			if err := r.Update(ctx, opiSvc); err != nil {
				return ctrl.Result{}, err
			}
		}
		return ctrl.Result{}, nil
	}

	// 4. Ensure finalizer.
	if !controllerutil.ContainsFinalizer(opiSvc, OPIFinalizer) {
		controllerutil.AddFinalizer(opiSvc, OPIFinalizer)
		if err := r.Update(ctx, opiSvc); err != nil {
			return ctrl.Result{}, err
		}
	}

	// 5. Validate the target DPUSet exists and is ready.
	targetDPUSet := &OPIDPUSet{}
	targetKey := types.NamespacedName{
		Name:      opiSvc.Spec.TargetDPUSet,
		Namespace: opiSvc.Namespace,
	}
	if err := r.Get(ctx, targetKey, targetDPUSet); err != nil {
		logger.Error(err, "Target OPIDPUSet not found", "targetDPUSet", opiSvc.Spec.TargetDPUSet)
		opiSvc.Status.Phase = "Error"
		opiSvc.Status.Conditions = []metav1.Condition{
			{
				Type:               "TargetDPUSetReady",
				Status:             metav1.ConditionFalse,
				LastTransitionTime: metav1.Now(),
				Reason:             "TargetNotFound",
				Message:            fmt.Sprintf("OPIDPUSet %q not found", opiSvc.Spec.TargetDPUSet),
			},
		}
		_ = r.Status().Update(ctx, opiSvc)
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, nil
	}

	if targetDPUSet.Status.Phase != "Ready" {
		logger.Info("Target DPUSet not ready, waiting", "phase", targetDPUSet.Status.Phase)
		opiSvc.Status.Phase = "Pending"
		opiSvc.Status.Conditions = []metav1.Condition{
			{
				Type:               "TargetDPUSetReady",
				Status:             metav1.ConditionFalse,
				LastTransitionTime: metav1.Now(),
				Reason:             "WaitingForDPUSet",
				Message:            fmt.Sprintf("Waiting for OPIDPUSet %q to become Ready (current: %s)", opiSvc.Spec.TargetDPUSet, targetDPUSet.Status.Phase),
			},
		}
		_ = r.Status().Update(ctx, opiSvc)
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, nil
	}

	// 6. Ensure DPF DPUService.
	_, err := r.Adapter.EnsureDPUService(ctx, opiSvc)
	if err != nil {
		logger.Error(err, "Failed to ensure DPUService")
		opiSvc.Status.Phase = "Error"
		_ = r.Status().Update(ctx, opiSvc)
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}

	// 7. Sync status from DPF.
	dpfSvcName := opiSvc.Name + "-dpf"
	dpfSvcStatus, err := r.Adapter.GetDPUServiceStatus(ctx, dpfSvcName, DPFNamespace)
	if err != nil {
		logger.Error(err, "Failed to get DPUService status")
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, err
	}

	opiPhase, conditions := r.StatusMapper.MapDPUServicePhase(*dpfSvcStatus)
	opiSvc.Status.Phase = opiPhase
	opiSvc.Status.Conditions = conditions
	opiSvc.Status.VendorStatus = &NvidiaSvcVendorStatus{
		DPFDPUServiceName:  dpfSvcName,
		DPFDPUServicePhase: dpfSvcStatus.Phase,
	}

	// Add sync condition.
	opiSvc.Status.Conditions = append(opiSvc.Status.Conditions, metav1.Condition{
		Type:               "DPFServiceSynced",
		Status:             metav1.ConditionTrue,
		LastTransitionTime: metav1.Now(),
		Reason:             "SyncSuccessful",
		Message:            "DPF DPUService is synced",
	})

	if err := r.Status().Update(ctx, opiSvc); err != nil {
		logger.Error(err, "Failed to update OPIDPUService status")
		return ctrl.Result{}, err
	}

	if opiPhase != "Deployed" {
		return ctrl.Result{RequeueAfter: DefaultRequeueInterval}, nil
	}

	logger.Info("OPIDPUService reconciliation complete", "phase", opiPhase)
	return ctrl.Result{}, nil
}

