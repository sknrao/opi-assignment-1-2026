// StatusMapper translates DPF phase strings into OPI status conditions.
type StatusMapper struct{}

// MapDPUSetPhase converts a DPF DPUSet phase string into an OPI phase and conditions.
func (m *StatusMapper) MapDPUSetPhase(dpfStatus DPUSetStatus) (string, []metav1.Condition) {
	now := metav1.Now()
	var opiPhase string
	var conditions []metav1.Condition

	switch dpfStatus.Phase {
	case "Ready":
		opiPhase = "Ready"
		conditions = append(conditions, metav1.Condition{
			Type:               "DPUsReady",
			Status:             metav1.ConditionTrue,
			LastTransitionTime: now,
			Reason:             "AllDPUsProvisioned",
			Message:            fmt.Sprintf("%d of %d DPUs ready", dpfStatus.ReadyCount, dpfStatus.TotalCount),
		})
	case "Provisioning":
		opiPhase = "Provisioning"
		conditions = append(conditions, metav1.Condition{
			Type:               "DPUsReady",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: now,
			Reason:             "ProvisioningInProgress",
			Message:            fmt.Sprintf("%d of %d DPUs ready", dpfStatus.ReadyCount, dpfStatus.TotalCount),
		})
	case "Error":
		opiPhase = "Error"
		conditions = append(conditions, metav1.Condition{
			Type:               "DPUsReady",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: now,
			Reason:             "DPFError",
			Message:            "DPF DPUSet reported an error",
		})
	default:
		opiPhase = "Unknown"
		conditions = append(conditions, metav1.Condition{
			Type:               "DPUsReady",
			Status:             metav1.ConditionUnknown,
			LastTransitionTime: now,
			Reason:             "UnknownDPFPhase",
			Message:            fmt.Sprintf("Unrecognized DPF phase: %s", dpfStatus.Phase),
		})
	}

	return opiPhase, conditions
}

// MapDPUServicePhase converts a DPF DPUService phase into an OPI phase and conditions.
func (m *StatusMapper) MapDPUServicePhase(dpfStatus DPUServiceStatus) (string, []metav1.Condition) {
	now := metav1.Now()
	var opiPhase string
	var conditions []metav1.Condition

	switch dpfStatus.Phase {
	case "Ready":
		opiPhase = "Deployed"
		conditions = append(conditions, metav1.Condition{
			Type:               "ServiceHealthy",
			Status:             metav1.ConditionTrue,
			LastTransitionTime: now,
			Reason:             "AllReplicasReady",
			Message:            "DPUService is healthy",
		})
	case "Deploying":
		opiPhase = "Deploying"
		conditions = append(conditions, metav1.Condition{
			Type:               "ServiceHealthy",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: now,
			Reason:             "DeploymentInProgress",
			Message:            "DPUService is being deployed",
		})
	case "Error":
		opiPhase = "Error"
		conditions = append(conditions, metav1.Condition{
			Type:               "ServiceHealthy",
			Status:             metav1.ConditionFalse,
			LastTransitionTime: now,
			Reason:             "DPFServiceError",
			Message:            "DPF DPUService reported an error",
		})
	default:
		opiPhase = "Pending"
	}

	return opiPhase, conditions
}

// NvidiaDPFAdapter implements DPFAdapter using a controller-runtime client
// to manage DPF CRDs in the same Kubernetes cluster.
type NvidiaDPFAdapter struct {
	Client       client.Client
	Scheme       *runtime.Scheme
	DPFNamespace string
}

// NewNvidiaDPFAdapter creates a new adapter with defaults.
func NewNvidiaDPFAdapter(c client.Client, scheme *runtime.Scheme) *NvidiaDPFAdapter {
	return &NvidiaDPFAdapter{
		Client:       c,
		Scheme:       scheme,
		DPFNamespace: DPFNamespace,
	}
}

func (a *NvidiaDPFAdapter) dpfName(opiName string) string {
	return opiName + "-dpf"
}

// parseVendorExtensions deserializes the opaque VendorExtensions blob into
// a NvidiaVendorExtensions struct. Returns nil if no extensions are present.
func (a *NvidiaDPFAdapter) parseVendorExtensions(opiDPUSet *OPIDPUSet) (*NvidiaVendorExtensions, error) {
	if opiDPUSet.Spec.VendorExtensions == nil || opiDPUSet.Spec.VendorExtensions.Raw == nil {
		return nil, nil
	}
	ext := &NvidiaVendorExtensions{}
	if err := json.Unmarshal(opiDPUSet.Spec.VendorExtensions.Raw, ext); err != nil {
		return nil, fmt.Errorf("failed to deserialize NVIDIA vendor extensions: %w", err)
	}
	return ext, nil
}

// EnsureBFB creates or updates a BFB resource from an OPIDPUSet spec.
func (a *NvidiaDPFAdapter) EnsureBFB(ctx context.Context, opiDPUSet *OPIDPUSet) (*BFB, error) {
	logger := log.FromContext(ctx)

	bfb := &BFB{
		ObjectMeta: metav1.ObjectMeta{
			Name:      a.dpfName(opiDPUSet.Name) + "-bfb",
			Namespace: a.DPFNamespace,
		},
	}

	// Determine BFB URL: prefer vendor extension override, fall back to firmware spec.
	bfbURL := opiDPUSet.Spec.FirmwareImage.URL
	ext, err := a.parseVendorExtensions(opiDPUSet)
	if err != nil {
		return nil, err
	}
	if ext != nil && ext.BFBURL != "" {
		bfbURL = ext.BFBURL
	}

	op, err := controllerutil.CreateOrUpdate(ctx, a.Client, bfb, func() error {
		if err := controllerutil.SetControllerReference(opiDPUSet, bfb, a.Scheme); err != nil {
			return err
		}
		bfb.Spec.URL = bfbURL
		bfb.Spec.Version = opiDPUSet.Spec.FirmwareImage.Version
		return nil
	})
	if err != nil {
		return nil, err
	}
	logger.Info("EnsureBFB completed", "name", bfb.Name, "operation", op)
	return bfb, nil
}

